    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();//pegar linha
       Funcionario funcionario = new Funcionario();
       funcionario.setIdFuncionario(Integer.parseInt(jTable1.getValueAt(linhaSelecionada, 0).toString()));
       JOptionPane.showMessageDialog(null, funcionario.getIdFuncionario());
       listar.excluir(funcionario);
       modelo.deletarLinha(linhaSelecionada);
    }//GEN-LAST:event_jButtonRemoverActionPerformed

